// FOR
console.log("FOR cycle");
for(var i = 0; i < 0; i++){
    console.log("jarda" + i);
}

// DO WHILE cycle
console.log("\nDO WHILE cycle");
i = 0;
do {
    console.log("jarda" + i);
    i++;
}while (i < 0);

// WHILE cycle
console.log("\nWHILE cycle");
i = 0;
while (i < 0){
    console.log("jarda" + i);
    i++;
}


